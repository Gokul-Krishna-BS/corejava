package mktclass;

public class ConditionalOperator {

	public static void main(String[] args)
	{
		String name1 = "Ram";
		String name2 = "Sita";
		char gender1 = 'M';
		char gender2 = 'F';
		
		if(name1=="Ram" && gender1=='M')
			System.out.println("Student 1");
		if(name2=="Sita" && gender2=='F')
			System.out.println("Student 2");
		
		
	}

}
