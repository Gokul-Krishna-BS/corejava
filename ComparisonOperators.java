package mktclass;

public class ComparisonOperators {

	public static void main(String[] args) 
	{
		int a=10;
		double b=34.98;
		if (a<b)
			System.out.println(b);
		else
			System.out.println(a);
		if(a<=b)
			System.out.println(a);
		if(a>=b)
			System.out.println(b);
		if(a==b)
			System.out.println("a and b are equal");
		if(a!=b)
			System.out.println("a and b are not equal");
	}

}
